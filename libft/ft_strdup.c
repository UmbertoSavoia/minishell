#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*copy;

	len = ft_strlen(s) + 1;
	if (!(copy = malloc(len)))
		return (0);
	ft_memcpy(copy, s, len);
	return (copy);
}
